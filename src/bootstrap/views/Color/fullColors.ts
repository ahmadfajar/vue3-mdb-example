export function useFullColorClasses(base: string, prop: string) {
  return {
    [`bg-${base}`]: true,
    [prop]: prop !== 'base',
    'text-white':
      ![
        'lighten-5',
        'lighten-4',
        'lighten-3',
        'lighten-2',
        'lighten-1',
        'accent-1',
        'accent-2',
        'accent-3',
      ].includes(prop) &&
      !(['lime', 'amber'].includes(base) && prop === 'base') &&
      !(base === 'yellow' && prop !== 'darken-4'),
    'text-black':
      [
        'lighten-5',
        'lighten-4',
        'lighten-3',
        'lighten-2',
        'lighten-1',
        'accent-1',
        'accent-2',
        'accent-3',
      ].includes(prop) ||
      (['lime', 'amber'].includes(base) && prop === 'base') ||
      (base === 'yellow' && prop !== 'darken-4'),
  };
}

export function useFullColorName(base: string, prop: string): string {
  if (prop === 'base') {
    return base;
  } else {
    return `${base} ${prop}`;
  }
}
